        'phone',
        'email',
        'category',
        'address',
        'note',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        //
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
     */
    protected $appends = [
        //
    ];

    /**
     * Get the discounts for the user.
     */
    public function discounts()
    {
        return $this->hasMany(CustomerDiscount::class);
    }
}
